/*
\' => single qoute
\" => double qoute
\n => new line
\\ => backslash
\r => carriage return
\t => tab
\b => backspace 
\f => form feed
*/

const myStr = "first line\n\tsecondline\nthirdline";
console.log(myStr);
/*
first line
    secondline
thirdline
*/

var code = "public static void main{\n\tint x=10;\n}"
console.log(code);
/*
public static void main{
    int x = 10;
}
*/