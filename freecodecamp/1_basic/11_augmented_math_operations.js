// Compound Assignment With Augmented Operations

var x = 10;
x += 10;
console.log(x); // Output : 20


var y = 20;
y -= 5;
console.log(y); // Output : 15

var z = 30;
z *= 2;
console.log(z); // Output : 60


var m = 40;
m /= 2;
console.log(m); // Output : 20


var pq = 10;
pq **= 2;
console.log(pq);//Ouput : 100