// Undefined
var x;
console.log(x);
//Output : undefined


//Null
var y = null;
console.log(y);
// Output : null

// Boolean
var flag = true;
console.log(flag);
// Output : true

// String
var name = "Airforce Academy";
console.log(name);
// Output : Airforce Academy

// Symbol
var sign = 'x';
console.log(sign);
// Output : x

// number
var cgpa = 3.50;
var roll = 127;
console.log(cgpa + " : " + roll);
// Output : 3.5 : 127

// decimal number
var value = 0.009;
console.log(value);
// Output : 0.009

// object
var person = { "name": "Clark", "surname": "Kent", "age": "36" };
console.log(person);
// Output : { name: 'Clark', surname: 'Kent', age: '36' }


