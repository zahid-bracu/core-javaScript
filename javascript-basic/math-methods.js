//Absolute
var number=3.50;
number=Math.abs(number);
console.log(number);
 
//rounding number
var number1=3.50;
number1=Math.round(number1);
console.log(number1);


//flooring a number
var number2=3.50;
number2=Math.floor(number2);
console.log(number2);


//ceiling a number
var number3=3.50;
number3=Math.ceil(number3);
console.log(number3);


//random number
var number4=Math.random();
console.log(number4);
 
 
 
 
 