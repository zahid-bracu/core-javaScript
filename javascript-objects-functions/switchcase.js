var num=60;
switch(num){
    case 10:
        console.log("Ten");
        break;
    case 20:
        console.log("Twenty");
        break;
    case 30:
        console.log("Thirty");
        break;
    case 50:
    case 40:
        console.log("fifty or forty");
        break;
    case 90:
    case 80:
        console.log("ninety or eighty");
        break;
    default:
        console.log("Not present");
        break;
}